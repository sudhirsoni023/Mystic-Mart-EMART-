    private void loadReceptionistId() {
	try
	{
	    List<String> list = ReceptionistDAO.getAllReceptionistUserId();
	    if(list.isEmpty())
	    {
		JOptionPane.showMessageDialog(null,"No Receptionist ID","Empty!",JOptionPane.ERROR_MESSAGE);
		    return;
	    }
	    btnRemove.setEnabled(true);
	    list.forEach((userid) -> {
		jcReceptionistId.addItem(userid);
	    });
	}
	catch(SQLException ex)
	{
	    JOptionPane.showMessageDialog(null,"Error in DB","Error!",JOptionPane.ERROR_MESSAGE);
	    ex.printStackTrace();
	}
    }

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
	String userid = jcReceptionistId.getSelectedItem().toString();
	try
	{
	    boolean result = ReceptionistDAO.deleteReceptionist(userid);
	    if(result)
	    {
		JOptionPane.showMessageDialog(null,"Receptionist Deleted Successfully!","Success!",JOptionPane.INFORMATION_MESSAGE);
		    jcReceptionistId.removeItemAt(jcReceptionistId.getSelectedIndex());
		    if(jcReceptionistId.getItemCount()==0)
			btnRemove.setEnabled(false);
		    return;
	    }
	    JOptionPane.showMessageDialog(null,"Receptionist not Deleted!","Error!",JOptionPane.ERROR_MESSAGE);
	}
	catch(SQLException ex)
	{
	    JOptionPane.showMessageDialog(null,"Error in DB","Error!",JOptionPane.ERROR_MESSAGE);
	    ex.printStackTrace();
	}
    }//GEN-LAST:event_btnRemoveActionPerformed

